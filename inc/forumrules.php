Fermer le règlement




<b>Accès</b>

Ce forum est ouvert à tous. 
Pour y contribuer, il vous sera demandé d'indiquer un pseudonyme et de saisir une adresse de courrier électronique.


<b>Droits et devoirs du lecteur</b>

Tout lecteur de ce forum doit respecter :
les droits de propriété intellectuelle des auteurs. 
Il doit notamment veiller à ne pas reproduire et diffuser les contributions publiées sur ce forum sur d’autres forums ou d’autres supports sans l’accord de leurs auteurs.
Tout lecteur peut néanmoins reproduire le contenu de ce forum à des fins de consultations privées ou reproduire et diffuser de courts extraits d’un message, à des fins d’information ou de recherches, en citant le nom de ce forum et le pseudo l’auteur du message.


<b>Droits et devoirs du contributeur</b>

Tout contributeur demeure propriétaire de son message et des droits de propriété intellectuelle attachés.
Les propos tenus sur ce forum sont néanmoins publiés sous votre propre responsabilité. 
A ce titre, vous devez respecter les lois et règlements en vigueur et le droit des personnes.
Par exemple, vous ne devez pas diffuser des messages violents, injurieux, diffamatoires, racistes, révisionnistes, faisant l’apologie des crimes de guerre, pédophiles, appelant au meurtre ou incitant au suicide, incitant à la discrimination ou à la haine ou dont le contenu reproduirait sans autorisation une œuvre protégée par la propriété intellectuelle (ex. articles de presse, message d’un tiers).
Veillez par ailleurs à respecter la courtoisie nécessaire au bon déroulement des débats.


<b>Modération</b>

Ce forum est modéré a posteriori, c’est-à-dire que le modérateur s'assure que votre contribution porte bien sur le thème de débat traité, respecte les loi et règlements en vigueurs et qu'elle permet un débat constructif. 
Il se réserve notamment la possibilité d'effacer les messages redondants, de nature publicitaire ou promotionnelle, contraires à l’ordre public ou aux bonnes mœurs, violents, racistes, révisionnistes, faisant l’apologie des crimes de guerre, injurieux ou grossiers, contraires aux droits d’auteur ou droits voisins, au droit applicable aux bases de données, au droit des marques, au droit à l’image, au droit au respect de la vie privée ou qui enfreindraient toute autre disposition législative ou réglementaire en vigueur.


<b>Destination des données collectées et des messages archivés</b>

Ce forum détient et conserve toutes données de nature à permettre l’identification des auteurs des messages et procède à l’archivage privé des messages postés. 
Ces informations ne pourront être communiquées qu’à une autorité judiciaire qui en formulerait la demande.


<b>Modification et suppression des messages</b>

Vous pouvez à tout moment nous demander la modification ou la suppression de vos propres messages, en prenant soin de nous indiquer les éléments suivants : titre et date de publication du message, pseudo utilisé.


<b>Protection des données personnelles</b>

Conformément à l’article 34 de la loi "Informatique et Libertés" du 6 janvier 1978, vous disposez d'un droit d'accès, de modification, de rectification et de suppression des données qui vous concernent.
Vous pouvez exercer ce droit en envoyant un message au propriétaire de ce forum
